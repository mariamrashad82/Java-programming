package day15_logicallops_switch_ternary;

public class SwitchStatement {
    public static void main(String[] args) {
        int n = 2;
        switch (n){
        case 1:
         System.out.println("n is 1");
         break;
         case 2:
         System.out.println("n is 2");
         break;
         case 3:
         System.out.println("n is 3");

        }
    }
}
