package day14_multi_branch_if_statment;

public class DayActivity {
    public static void main(String[] args) {
        String weather = "SNOWY";
        if (weather == ("SUNNY")) {
            System.out.println(" GO HIKING AND CODE JAVA");
        } else if (weather == ("RAINY")) {
            System.out.println(" STAY HOME , AND CODE JAVA");
        } else if (weather == ("SNOWY")) {
            System.out.println("CLEAN YOUR CAR ,DRINK HOT CHOCOLATE , AND CODE JAVA");
        } else if (weather == ("WENDY")) {
            System.out.println("GET READY FOR POWER LOSE , AND CODE JAVA");
        } else {
            System.out.println(" CODING JAVA");
        }
    }
}